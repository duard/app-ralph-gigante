-- Query para investigar pedidos de compra do produto 3680 em dezembro 2025
SELECT 
    c.NUNOTA,
    c.DTMOV,
    c.DTNEG,
    c.DTMOV,
    c.STATUSNOTA,
    c.CODEMP,
    c.NUMCOTACAO,
    i.SEQUENCIA,
    i.QTDNEG,
    i.QTDENTREGUE,
    i.VLRUNIT,
    i.VLRTOT,
    t.DESCROPER,
    u.NOMEUSU
    us.NOMEUSU AS solicitante
FROM TGFCAB c
JOIN TGFITE i ON i.NUNOTA = c.NUNOTA
LEFT JOIN TGFTOP t ON t.CODTIPOPER = c.CODTIPOPER
LEFT JOIN TSIUSU u ON u.CODUSU = c.CODUSU
LEFT JOIN TSICUS us ON us.CODUSU = u.CODUSU
WHERE i.CODPROD = 3680
  AND c.TIPMOV = 'O'
  AND c.STATUSNOTA = 'L'
  AND MONTH(c.DTNEG) = 12
  AND YEAR(c.DTNEG) = 2025
  AND (t.DESCROPER LIKE '%APROVACAO%' 
       OR t.DESCROPER LIKE '%AUTORIZACAO%')
ORDER BY c.DTMOV DESC;